SELECT COUNT(*)                      
FROM (
    SELECT ФАМИЛИЯ, ОТЧЕСТВО
    FROM Н_ЛЮДИ
    GROUP BY ФАМИЛИЯ, ОТЧЕСТВО
) AS unique_names;

